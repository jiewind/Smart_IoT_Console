/**
  * @brief  微秒级延时函数（利用 SysTick 实现）
  * @param  us: 要延时的微秒数
  * @retval 无
  */
void delay_us(uint32_t us)
{
    uint32_t ticks;
    uint32_t told, tnow, tcnt = 0;
    uint32_t reload = SysTick->LOAD;           // 获取重装载寄存器的值
    
    ticks = us * (SystemCoreClock / 1000000);  // 计算需要走多少个时钟周期
    told = SysTick->VAL;                       // 刚进入时的计数器值
    
    while (1)
    {
        tnow = SysTick->VAL;
        if (tnow != told)
        {
            if (tnow < told)
            {
                tcnt += told - tnow;           // 正常递减计数
            }
            else
            {
                tcnt += reload - tnow + told;  // 发生了重装载翻转
            }
            told = tnow;
            if (tcnt >= ticks)
            {
                break;                         // 时间到了，退出
            }
        }
    }
}
